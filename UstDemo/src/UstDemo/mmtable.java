package UstDemo;

public class mmtable {

	public static void main(String[] args) {
		System.out.println("The 24's multiplication table is:");
		for(int i=1;i<11;i++) {
			int k=24*i;
			System.out.println(i+" * 24 = "+k);
		}
		System.out.println("The 50's multiplication table is:");
		for(int j=1;j<11;j++) {
			int l=50*j;
			System.out.println(j+" * 50 = "+l);
		}System.out.println("The 29's multiplication table is:");
		for(int n=1;n<11;n++) {
			int m=29*n;
			System.out.println(n+" * 29 = "+m);
		}

	}

}
